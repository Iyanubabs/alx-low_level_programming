/**
 * print_sign - Print the sign of a number
 *
 * @n: type int integer, can be negative or positive
 *
 * Description: print +, 0, or - depending on number, along with return
 *
 * Return: 1 if +, 0 if 0, and -1 if -
 */

int _isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}
